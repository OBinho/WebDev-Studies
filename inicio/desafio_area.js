/*
    Faça um código para entregar a área de uma circunferência.

    Fórmula para calcular a area de uma circunferência:
    PI  x raio²
*/

let raio = 73.45;

const PI = 3.141592;
const AREACIRC = PI * raio * raio;

console.log('O raio é de: ' + raio + 'm².');
console.log('Portanto a área de circunferência é de: '+ AREACIRC + 'm².');